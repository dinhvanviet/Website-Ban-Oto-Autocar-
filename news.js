// 2. Hàm hiển thị chi tiết tin tức
function showNewsDetail(id) {
  const news = newsData.find((n) => n.id === id);
  const modal = document.getElementById("detailModal");
  const modalBody = document.getElementById("modalBody");

  if (news && modal && modalBody) {
    modalBody.innerHTML = `
            <img src="${news.image}" style="width:100%; border-radius:8px; margin-bottom:15px; height:250px; object-fit:cover;">
            <h2 style="margin-bottom:10px; color:#1e293b;">${news.title}</h2>
            <p style="color:#64748b; font-size:14px; margin-bottom:15px;">📅 Đăng ngày: ${news.date}</p>
            <div style="line-height:1.8; color:#334155; text-align:justify;">
                ${news.content}
            </div>
            <button onclick="closeDetailModal()" style="margin-top:20px; width:100%; padding:12px; background:#ef4444; color:white; border:none; border-radius:5px; cursor:pointer; font-weight:bold;">ĐÓNG BÀI VIẾT</button>
        `;
    modal.classList.add("show");
  }
}

// Hàm đóng modal chi tiết tin tức
function closeDetailModal() {
  const modal = document.getElementById("detailModal");
  if (modal) {
    modal.classList.remove("show");
  }
}

// Thêm event listener cho nút đóng modal
document.addEventListener("DOMContentLoaded", function () {
  const modalClose = document.getElementById("modalClose");
  if (modalClose) {
    modalClose.addEventListener("click", closeDetailModal);
  }

  // Đóng modal khi click ra ngoài
  const modal = document.getElementById("detailModal");
  if (modal) {
    modal.addEventListener("click", function (e) {
      if (e.target === modal) {
        closeDetailModal();
      }
    });
  }
});
